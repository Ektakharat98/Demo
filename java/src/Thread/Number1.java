package Thread;

interface PosNegative{
	void print(int a);
	
}


public class Number1 {
public static void main(String[] args) {
	PosNegative posNegative = (int a) -> {
		
	if (a > 0) {
		System.out.println("Positive number:" +a);
	}else {
		System.out.println("Negative number:" +a);
	}
};
posNegative.print(25);
}
}
